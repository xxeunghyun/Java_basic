
public class increment_operator {

	public static void main(String[] args) {
	
		int a = 10;
		
		System.out.println("a = "+a);
		a++;
		System.out.println("a after a++ = "+a);
		System.out.println("++a = "+ ++a);
		System.out.println("a++ = "+ a++);	// 여기서 출력 후 a값 1 증
		System.out.println("a = "+a);
	}
}
