
public class ifstatement {

	public static void main(String[] args) {
		
		String a = "I LOVE YOU";
		
		if(a.contains("LOVE") == true)
			// ==true 는 생략해도 가넝~~  
		{
			System.out.println("It contains the word 'LOVE'");
		}
		
		else
		{
			System.out.println("It does not contain the word 'LOVE'");
		}
	}

}
